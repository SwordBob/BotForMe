# 使用准确映射
echo "📋 重命名映射表:"
echo "----------------------------------------"
for old_name in "${!accurate_map[@]}"; do
    new_name="${accurate_map[$old_name]}"
    echo "$old_name → $new_name"
done
echo "----------------------------------------"

echo ""
echo "❓ 确认执行重命名？(输入y确认): "
read -r confirm

if [[ "$confirm" != "y" && "$confirm" != "Y" ]]; then
    echo "❌ 取消重命名"
    exit 0
fi

# 执行重命名
cd "按章节拆分结果" || exit 1

success_count=0
total_count=0

for old_name in "${!accurate_map[@]}"; do
    new_name="${accurate_map[$old_name]}"
    
    if [[ -f "$old_name" ]]; then
        total_count=$((total_count + 1))
        
        # 如果新文件已存在，先备份
        if [[ -f "$new_name" ]]; then
            backup_name="${new_name%.pdf}.backup.pdf"
            mv "$new_name" "$backup_name"
            echo "📦 备份已存在文件: $new_name → $backup_name"
        fi
        
        # 执行重命名
        if mv "$old_name" "$new_name"; then
            echo "✅ 重命名成功: $old_name → $new_name"
            success_count=$((success_count + 1))
        else
            echo "❌ 重命名失败: $old_name"
        fi
    else
        echo "⚠️  文件不存在: $old_name"
    fi
done

echo ""
echo "📊 重命名结果:"
echo "  成功: $success_count/$total_count"
echo "  失败: $((total_count - success_count))"

# 显示重命名后的文件列表
echo ""
echo "📋 重命名后的文件列表:"
ls -1 *.pdf | sort

# 创建章节索引
echo ""
echo "📝 创建章节索引文件..."
