# 📚 信息系统项目管理师（第四版）章节索引

## 章节文件列表

| 章节编号 | 章节名称 | 页码范围 | 页数 |
|----------|----------|----------|------|
EOF

for pdf_file in $(ls *.pdf | sort); do
    if [[ $pdf_file =~ 第([0-9]+)章_([^_]+)_第([0-9]+)-([0-9]+)页\.pdf ]]; then
        chapter_num="${BASH_REMATCH[1]}"
        chapter_name="${BASH_REMATCH[2]}"
        start_page="${BASH_REMATCH[3]}"
        end_page="${BASH_REMATCH[4]}"
        pages=$((end_page - start_page + 1))
        
        echo "| 第${chapter_num}章 | ${chapter_name} | 第${start_page}-${end_page}页 | ${pages}页 |" >> "章节索引.md"
    fi
done

cat >> "章节索引.md" << 'EOF'

## 说明

1. 本目录包含《信息系统项目管理师教程（第四版）》按章节拆分的PDF文件
2. 文件名格式统一为：`第XX章_章节名称_第A-B页.pdf`
3. 章节名称已按教材实际内容命名
4. 页码对应原教材页码（1-indexed）

## 使用建议

- 按章节顺序学习：从第1章开始
- 重点章节：第5-12章（核心项目管理知识）
- 考试重点：第6章（项目进度管理）、第7章（项目成本管理）
- 复习策略：先理论后实践，多做练习题
EOF

echo "✅ 章节索引已创建: 章节索引.md"

